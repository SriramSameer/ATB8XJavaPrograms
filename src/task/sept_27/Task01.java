package task.sept_27;

public class Task01 {
    public static void main(String[] args) {
        //Example of while loop

        int i = 0;
        while (i<=10){

            i++;
            System.out.println(i);
        }

        System.out.println("For next line");
        //Example of do while loop

        int a = 0;
        do {
            a++;
            System.out.println(a);
        } while (a<10);
    }
}
